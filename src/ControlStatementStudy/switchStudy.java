package ControlStatementStudy;

public class switchStudy {

	public static void main(String[] args)
	{
		int year=4;
		
        switch (year)
        {
		case 1:  System.out.println("I am in first Year");
		break;
		
			
		case 2:  System.out.println("I am in second Year"); 
		break;
		case 3:  System.out.println("I am in Third Year");
		break;
		case 4:  System.out.println("I am in Last Year");
		break;
		
		default:
		 System.out.println(" Please enter Valid Year");
		break;
		
			
		}
		
        }
	

}
