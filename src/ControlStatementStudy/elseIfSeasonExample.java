package ControlStatementStudy;

public class elseIfSeasonExample {

	public static void main(String[] args) {
		
		String season;
		
		season="Summer";
		if (season == "Summer") {System.out.println("Season is Summer");}
		else if (season == "winter") {System.out.println("Season is Winter");}
		else if (season== "rainy") {System.out.println("Season is rainy");}
		else {System.out.println("Please enter valid season");}

	}

}
