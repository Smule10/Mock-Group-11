package Loop_Study;

public class Example {

	public static void main(String[] args) {
		//  Print Number which is divisible by 5  from 0 to 100..
		
		
		
		for (int i=1; i<=70; i++)
		{
			
			if (i%7 == 0 )
			{
				System.out.println(i);
			}
			
			
			
		}
			
		for (int i=1; i<=10; i=i+2)
		{
			System.out.println(i);
		}
		
		
      
	}

}
